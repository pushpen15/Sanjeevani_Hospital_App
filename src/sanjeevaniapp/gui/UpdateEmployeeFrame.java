 private void SetcomboBoxId(){
      ArrayList<String>idList=new ArrayList<>();
        try{
            idList=getAllId();
            for(String s:idList){
                jcbEmpId.addItem(s);
            }
        }
              catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"SQL error","Error!",JOptionPane.ERROR_MESSAGE);
        }
 }
    
    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        // TODO add your handling code here:
         if(validateInputs()==false)
      {
           JOptionPane.showMessageDialog(null,"Please  Fill All The Inputs","Error!",JOptionPane.ERROR_MESSAGE);
           return;
      }
       try{ 
         
          e.setEmpid(jcbEmpId.getSelectedItem().toString());
          e.setEmpname(txtEmpName.getText());
          e.setJob(jcbJob.getSelectedItem().toString());
           e.setSal(Double.parseDouble(txtSal.getText()));
      
       boolean x= updateEmp(e);
        btnUpdateEmp.setEnabled(false);
         txtEmpName.setText(e.getEmpname());
         txtEmpName.setEnabled(false);
         txtSal.setEnabled(false);
         txtSal.setText(""+e.getSal()); 
          jcbJob.setSelectedItem(e.getJob());
           jcbJob.setEnabled(false);
       if(x==false)
       {JOptionPane.showMessageDialog(null,"Update unsuccessfull","Error!",JOptionPane.ERROR_MESSAGE);
        return;}
       
         JOptionPane.showMessageDialog(null,"Record Saved Successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
       }
        catch(NumberFormatException nfe)
        {
            JOptionPane.showMessageDialog(null,"Please Input Digits only","Error!",JOptionPane.ERROR_MESSAGE);
        }
       
        catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"SQL error"+ex,"Error!",JOptionPane.ERROR_MESSAGE);
        } catch (InterruptedException ex) {
           
        }
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

