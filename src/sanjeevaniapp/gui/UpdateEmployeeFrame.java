    public void loadEmpDetails()
    {
         try{
         
     
        e=findEmpById(jcbEmpId.getSelectedItem().toString());
        if(e==null){
             JOptionPane.showMessageDialog(null,"No Such Employee Found","Error!",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
            
            btnUpdateEmp.setEnabled(true);
            txtEmpName.setText(e.getEmpname());
            txtEmpName.setEnabled(true);
            txtSal.setEnabled(true);
            txtSal.setText(""+e.getSal()); 
            jcbJob.setSelectedItem(e.getJob());
            jcbJob.setEnabled(true);
      } 
       catch(NumberFormatException nfe)
        {
            JOptionPane.showMessageDialog(null,"Please Input Digits only","Error!",JOptionPane.ERROR_MESSAGE);
        }
        catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"SQL error","Error!",JOptionPane.ERROR_MESSAGE);
        }
    }
